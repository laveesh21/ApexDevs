/**
 * Get the selected avatar based on user's preference
 * @param {Object} user - User object with avatar, identicon, and avatarPreference fields
 * @returns {string} - The URL of the selected avatar
 */
export const getSelectedAvatar = (user) => {
  if (!user) return '';
  
  // If user prefers custom avatar and has one, use it
  if (user.avatarPreference === 'custom' && user.avatar) {
    return user.avatar;
  }
  
  // Otherwise, use identicon

  
  return user.identicon || '';
};





















































































































































